    public String sendMail(String Remetente,String Senha,String Destinatario,String Assunto,String TextMensagem, String txtFile){

	String Msg;
    
        final String usuario = Remetente;
        final String senha = Senha;
        
        Properties props = new Properties();
        props.put("mail.smtp.auth", true);
        props.put("mail.smtp.starttls.enable", true);
        props.put("mail.smtp.host", "smtp.gmail.com");
        props.put("mail.smtp.port", "587");
        props.setProperty("mail.smtp.ssl.trust", "smtp.gmail.com");

        Session session = Session.getInstance(props, new Authenticator() {
            @Override
            protected PasswordAuthentication getPasswordAuthentication() {
                return new PasswordAuthentication(usuario, senha);
            }
        });

        try {

            Message message = new MimeMessage(session);
            message.setFrom(new InternetAddress(Remetente));
            message.setRecipients(Message.RecipientType.TO, InternetAddress.parse(Destinatario));
            message.setSubject(Assunto);
            message.setText(TextMensagem);
            message.setText(txtFile);
            Transport.send(message);
            Msg="true";
    	    return Msg;

        } catch (MessagingException e) {
            return e.toString();
        }
    
    
    
    }
